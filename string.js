//Check Whether a String Starts and Ends With Certain Characters

const myString = "Hello, World!";

// Check if the string starts with "Hello"
const startsWithHello = myString.startsWith("Hello");

// Check if the string ends with "World!"
const endsWithWorld = myString.endsWith("World!");

// Display the results
console.log(`String starts with "Hello": ${startsWithHello}`);
console.log(`String ends with "World!": ${endsWithWorld}`);
